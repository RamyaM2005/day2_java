public class Swap {
    public static void main(String[] args) {
        int boy=40,girl=30;
        int temp;
        System.out.println("before swap boy:"+boy);
        System.out.println("before swap girl:"+girl);
        temp=girl;
        girl=boy;
        boy=temp;
        System.out.println("After swap boy:"+boy);
        System.out.println("After swap girl:"+girl);


    }
    
}
